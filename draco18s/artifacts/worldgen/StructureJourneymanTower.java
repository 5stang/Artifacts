	private void basement(World world, int i, int j, int k) {
		boolean noair = false;
		int y = -1;
		do {
			noair = false;
			for(int x = 1; x <= 9; x++) {
				for(int z = 0; z <= 8; z++) {
					//5,y,4
					int d = (x-5)*(x-5)+(z-4)*(z-4);
					if(d <= 17) {
						if(!world.isBlockOpaqueCube(i+x, j+y, z+k) || world.getBlockId(i+x, j+y, z+k) == Block.leaves.blockID) {
							noair=true;
							world.setBlock(i+x, j+y, z+k, Block.cobblestone.blockID, 0, 2);
						}
					}
				}
			}
			y--;
		} while(noair && (j+y) >= 0);
		if(y >= -7 && world.rand.nextBoolean()) {
			y = -8;
		}
		if(y < -7) {
			y++;
			int yy = 3;
			for(; yy <= 5; yy++) {
				for(int x = 3; x <= 7; x++) {
					for(int z = 2; z <= 6; z++) {
						world.setBlockToAir(i+x, j+y+yy, k+z);
					}
				}
			}
			world.setBlock(i+5, j+y+6, k+4, Block.mobSpawner.blockID, 0, 2);
			TileEntityMobSpawner tileentitymobspawner = (TileEntityMobSpawner)world.getBlockTileEntity(i+5, j+y+6, k+4);

            if (tileentitymobspawner != null)
            {
                tileentitymobspawner.getSpawnerLogic().setMobID("Artifacts.EntClayGolem");
                NBTTagCompound nbt = new NBTTagCompound();
                tileentitymobspawner.writeToNBT(nbt);
                nbt.setShort("MinSpawnDelay",(short)800);
                nbt.setShort("MaxSpawnDelay",(short)2400);
                tileentitymobspawner.readFromNBT(nbt);
            }
			for(yy=3;yy*-1>y;yy++) {
				world.setBlock(i+7, j+y+yy, k+6, Block.ladder.blockID, 2, 2);
			}
			world.setBlock(i+7, j+y+yy, k+6, Block.ladder.blockID, 2, 2);
			world.setBlock(i+7, j+y+yy+1, k+6, Block.trapdoor.blockID, 2, 2);
		}
	}
