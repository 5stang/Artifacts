	private void basement(World world, int i, int j, int k) {
		boolean noair = false;
		int y = -1;
		do {
			noair = false;
			for(int x = 1; x <= 12; x++) {
				for(int z = 0; z <= 12; z++) {
					//5,y,4
					int d = (x-6)*(x-6)+(z-7)*(z-7);
					if(d <= 26) {
						if(!world.isBlockOpaqueCube(i+x, j+y, z+k) || world.getBlockId(i+x, j+y, z+k) == Block.leaves.blockID) {
							noair=true;
							world.setBlock(i+x, j+y, z+k, Block.stoneBrick.blockID, 0, 2);
						}
					}
				}
			}
			y--;
		} while(noair && (j+y) >= 0);
		if(y >= -7 && world.rand.nextInt(4) != 0) {
			y = -8;
		}
		if(y < -7) {
			y++;
			int yy = 3;
			for(; yy <= 5; yy++) {
				for(int x = 3; x <= 8; x++) {
					for(int z = 4; z <= 8; z++) {
						world.setBlockToAir(i+x, j+y+yy, k+z);
					}
				}
			}
			world.setBlock(i+5, j+y+6, k+6, Block.mobSpawner.blockID, 0, 2);
			TileEntityMobSpawner tileentitymobspawner = (TileEntityMobSpawner)world.getBlockTileEntity(i+5, j+y+6, k+6);

            if (tileentitymobspawner != null)
            {
                tileentitymobspawner.getSpawnerLogic().setMobID("DragArti.EntClayGolem");
                NBTTagCompound nbt = new NBTTagCompound();
                tileentitymobspawner.writeToNBT(nbt);
                nbt.setShort("MinSpawnDelay",(short)800);
                nbt.setShort("MaxSpawnDelay",(short)2400);
                tileentitymobspawner.readFromNBT(nbt);
            }
			for(yy=3;yy*-1>y-3;yy++) {
				world.setBlock(i+9, j+y+yy, k+6, Block.ladder.blockID, 2, 2);
			}
			//world.setBlock(i+9, j+y+yy-1, k+6, Block.ladder.blockID, 2, 2);
			world.setBlock(i+9, j+y+yy, k+6, Block.trapdoor.blockID, 1, 2);
		}
	}
